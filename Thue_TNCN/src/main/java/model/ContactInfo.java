package model;

public class ContactInfo {
    private int id, userId;
    private String phone;
    private String email;

    public ContactInfo() {
    }

    public ContactInfo(int id, int userId, String phone, String email) {
        this.id = id;
        this.userId = userId;
        this.phone = phone;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
